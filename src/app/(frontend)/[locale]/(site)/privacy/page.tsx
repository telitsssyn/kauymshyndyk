import type { Metadata } from 'next'
import { getTranslations, setRequestLocale } from 'next-intl/server'

import { BrushHeading } from '@/components/BrushHeading'
import { Link } from '@/i18n/navigation'
import { getSettings } from '@/lib/queries'

export const revalidate = 3600

const LAST_UPDATED = '18 июля 2026 года'

export async function generateMetadata(): Promise<Metadata> {
  const t = await getTranslations('legal')
  return { title: t('privacyTitle') }
}

export default async function PrivacyPage({
  params,
}: {
  params: Promise<{ locale: string }>
}) {
  const { locale } = await params
  setRequestLocale(locale)

  const [settings, t] = await Promise.all([getSettings(), getTranslations('legal')])
  const churchName = settings?.churchName ?? 'Кауым-Церковь Шындык'

  return (
    <div className="container-site py-10 sm:py-14">
      <BrushHeading as="h1" className="text-4xl sm:text-5xl">
        {t('privacyTitle')}
      </BrushHeading>
      <p className="mt-4 text-base text-ink-soft">{t('updated', { date: LAST_UPDATED })}</p>

      <div className="prose-church mt-8 max-w-3xl">
        <h2>1. Общие положения</h2>
        <p>
          Эта политика объясняет, какие сведения обрабатываются, когда вы посещаете сайт церкви
          «{churchName}» (г. Павлодар), и как мы с ними обращаемся. Мы собираем минимум данных и
          соблюдаем Закон Республики Казахстан от 21 мая 2013 года № 94-V «О персональных данных и
          их защите».
        </p>

        <h2>2. Какие данные мы собираем</h2>
        <p>
          На сайте нет регистрации, личных кабинетов и форм обратной связи. Мы не просим вас
          вводить имя, телефон, почту или другие персональные данные.
        </p>
        <p>
          При посещении сайта автоматически обрабатываются технические сведения: IP-адрес, тип
          браузера и устройства, дата и время визита, адреса просмотренных страниц. Эти данные
          попадают в служебные журналы хостинг-провайдера и нужны для защиты от атак и поддержания
          работы сайта. Мы не используем их, чтобы установить личность посетителя.
        </p>

        <h2>3. Файлы cookie</h2>
        <p>
          Сайт не использует cookie для рекламы или отслеживания посетителей. Служебные cookie
          могут устанавливаться только в административной части сайта (ею пользуются сотрудники
          церкви) и встроенными сервисами, о которых написано ниже. Вы можете отключить cookie в
          настройках браузера — сайт продолжит работать.
        </p>

        <h2>4. Сервисы других компаний</h2>
        <p>На некоторых страницах встроены материалы сторонних сервисов:</p>
        <ul>
          <li>
            видеозаписи проповедей — плеер <strong>YouTube</strong> (Google);
          </li>
          <li>
            карта проезда — виджет <strong>2ГИС</strong>;
          </li>
          <li>
            переводы пожертвований — приложение <strong>Kaspi.kz</strong> или банк по указанным
            реквизитам.
          </li>
        </ul>
        <p>
          Когда вы открываете страницу со встроенным видео или картой, ваш браузер обращается к
          серверам этих компаний, и они могут собирать данные по собственным правилам. Платежи
          сайт не обрабатывает и платёжные данные не хранит: перевод происходит в приложении банка.
        </p>

        <h2>5. Фотографии</h2>
        <p>
          На сайте публикуются фотографии со служений и событий церкви. Если вы увидели себя на
          фото и хотите, чтобы мы его убрали, — напишите нам, и мы удалим снимок.
        </p>

        <h2>6. Передача данных</h2>
        <p>
          Мы не продаём и не передаём сведения о посетителях третьим лицам. Технические журналы
          доступны только хостинг-провайдеру, который обеспечивает работу сайта.
        </p>

        <h2>7. Вопросы и обращения</h2>
        <p>
          По любым вопросам об обработке данных свяжитесь с нами удобным способом — контакты
          указаны на странице{' '}
          <Link href="/contacts">«Контакты»</Link>
          {settings?.email ? (
            <>
              {' '}
              или по почте <a href={`mailto:${settings.email}`}>{settings.email}</a>
            </>
          ) : null}
          .{settings?.address ? <> Адрес церкви: {settings.address}.</> : null}
        </p>

        <h2>8. Изменения политики</h2>
        <p>
          Мы можем обновлять эту политику. Актуальная версия всегда опубликована на этой странице,
          дата последнего обновления указана вверху.
        </p>
      </div>
    </div>
  )
}
